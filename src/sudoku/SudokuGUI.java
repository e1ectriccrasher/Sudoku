   private static String imprime(int mat[][]){
        if (mat == null) {
            throw new NullPointerException("No es valido");
        }
        StringBuilder cad= new StringBuilder();
        for(int i=0; i<=8; i++){
            cad.append("\n");
            for(int j=0; j<=8; j++){
              cad.append(mat[i][j]);
              
            }

            
        }
        return cad.toString();
    }
    
